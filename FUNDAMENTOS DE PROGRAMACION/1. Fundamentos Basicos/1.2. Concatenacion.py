
# CONCATENACION

# Signos de concatenacion = '+' '*' ','

    # + = Concatena cadenas. Permite juntar 2 argumento y unirlos en un solo argumento.
    # (String + String ...)
    # EJEMPLO

print("HOLA"+ ", COMO ESTAS?")
hola = input("Hola"+", Como estas?: ") #


    # , = Concatena cadenas y numeros. Permite juntar 2 argumento de tipo string y numerico, cada argumento es independiente de si.
        # OJO = El signo de concatenacion brinda un espacio ya definido entre los argumentos.
    # (String , Numero ...)
    # (Numero , String ...)
    # EJEMPLO

print("YERIC,"," QUE HACES?", 22)
input(hola+": ")

    # * = Concatena replicacion. Permite replicar una cadena de acuerdo a la cantidad de veces que brinda
    # (String * Numero)
    # (Numero * String)
    # EJEMPLO

print("YERIC" * 3)