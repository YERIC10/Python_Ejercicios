
# OPERADORES ARITMETICOS
    # Los operadores aritmeticos actuan de acuerdo a sus prioridades.

    # EJEMPLOS:
    # ( + , - ) = Prioridad Unario:      4
    # ( ** )    = Prioridad Unario:      3
    # ( * , / , // , % ) = Prioridad Unario:  2
    # ( + , - ) = Prioridad Binaria:     1

    # La prioridad mas 'ALTA' es: 4 y la prioridad mas 'BAJA' es: 1, es decir que actuan de acuerdo a la prioridad mas alta en una
    # operacion.

    # Se evalua de Derecha a Izquierda

print("\n\tOPERACIONES ARITMETICAS")
print("\n\t SUMA")
print("7 + 14 = ", 7+14)

print("\n\tRESTA")
print("7 - 14 = ", 7-14)

print("\n\tMULTIPLICACION")
print("7 * 14 = ", 7 * 14)

print("\n\tDIVISION")

print("Division Exacta")
print("7 / 14 = ", 7/14 )

print("\nDivision Entera")  # Esta division entera redondea a un valor inferior del resultado
print("7 // 14 = ", 7//14 )

print("\nResiduo de una Division")
print("7 % 14 = ", 7%14)

print("\n\tEXPONENCIAL")
print("7**14 = ", 7**14)
