#  QUE ES UNA TUPLA

# Es una secuencia de datos INUTABLES. Se puede comportar como una lista pero no puede ser modificada en el momento.
# cada elemento de una tupla puede ser de distinto tipo (punto flotante, entero, cadena, o cualquier otro tipo de dato).

# EJEMPLO 1 - Defincion de una Tupla

tuple_1 = (1, 2, 4, 8)
tuple_2 = 1., .5, .25, .125

print(tuple_1)
print(tuple_2)


