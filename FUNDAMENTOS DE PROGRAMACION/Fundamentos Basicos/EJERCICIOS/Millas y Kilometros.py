
# Millas y kilómetros son unidades de longitud o distancia.

# Teniendo en mente que 1 milla equivale aproximadamente a 1.61 kilómetros, complementa el programa en el editor
# para que convierta de:

#   Millas a kilómetros.
#   Kilómetros a millas.


kilometros = 12.25
millas = 7.38

milla_a_kilometro = millas * 1.61
kilometro_a_milla = kilometros / 1.61

print(millas, "millas son", round(milla_a_kilometro, 2), "kilómetros")
print(kilometros, "kilómetros son", round(kilometro_a_milla, 2), "millas")